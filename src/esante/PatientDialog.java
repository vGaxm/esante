    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (!((AdminForm)getParent()).requestLogin()) return;
        Patient p = new Patient();
        p.data[0] = tfNss.getText();
        p.data[1] = tfNom.getText();
        p.data[2] = tfPrenom.getText();
        p.data[3] = new java.sql.Date(datePicker1.getSelectedDate().getTime());
        p.data[4] = tfAdresse.getText();
        p.data[5] = tfTel.getText();
        p.data[6] = tfEmail.getText();
        p.data[7] = (buttonGroup1.getSelection()).getActionCommand();
        System.out.println((buttonGroup1.getSelection()).getActionCommand());
        p.data[8] = (Integer)jSpinner1.getValue();
        p.data[9] = (Integer)jSpinner2.getValue();
        p.data[11] = tfAler.getText();
        Patient.insert(((AdminForm)getParent()).session, p);
        dispose();
    }//GEN-LAST:event_jButton1ActionPerformed

